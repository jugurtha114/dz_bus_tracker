  // Get icon by name
  static IconData? getIcon(String name) {
    switch (name.toLowerCase()) {
      case 'bus': return bus;
      case 'user': return user;
      case 'dashboard': return dashboard;
      case 'settings': return settings;
      case 'profile': return profile;
      case 'map': return map;
      case 'location': return location;
      case 'notifications': return notifications;
      case 'help': return help;
      case 'logout': return logout;
      case 'admin': return admin;
      case 'driver': return driver;
      case 'passenger': return passenger;
      case 'analytics': return analytics;
      case 'performance': return performance;
      case 'schedule': return schedule;
      case 'payment': return payment;
      case 'safety': return safety;
      default: return null;
    }
  }
  
  // Get themed icon color
  static Color getIconColor(BuildContext context, {bool isSelected = false}) {
    final theme = Theme.of(context);
    if (isSelected) {
      return theme.colorScheme.primary;
    }
    return theme.colorScheme.onSurface.withValues(alpha: 0.7);
  }
  
  // Get icon with theme-aware color
  static Widget themedIcon(
    IconData icon, {
    required BuildContext context,
    double? size,
    bool isSelected = false,
    Color? color,
  }) {
    return Icon(
      icon,
      size: size ?? 24,
      color: color ?? getIconColor(context, isSelected: isSelected),
    );
  }
